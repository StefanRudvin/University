                <h3 class="section-header">Developing Understanding, Infrastructure &amp; Engagement</h3>

                <p class="lead text-muted">There is now a broad consensus that new forms of social data emerging from people&apos;s day-to-day activities on the Web have the potential to transform the social sciences. However, there is also agreement that the current analytical techniques fall short of the methodological standards required for academic research and policy making; and that conclusions drawn from social media data have much greater utility when combined with results drawn from other datasets (including the various public sector resources made available through open data initiatives).</br></br> Human sense making is also seen as playing an important role. Areas in which social media data have the potential to provide insights include: social order and civil society, politics and trust, sentiment, consumption, health, information diffusion, creativity and the arts.</p>

                <p class="lead text-muted">Building on existing research and expertise at the University of Aberdeen, this interdisciplinary portfolio of activities involves 26 researchers from 7 different disciplines.</p>

                <h4 class="section-header">Activities are organised by four strands:</h4>
                <h3 class="section-header">Understanding Attitudes &amp; Impacts</h3>
                <ul class="lead text-muted">
                  <li>Exploration of Twitter Data Use in Transport Disruption</li>
                  <li>Social Media, Privacy and Risk: Towards More Ethical Research Methodologies [Sociology]</li>
                </ul>

                <h3 class="section-header">e-Infrastructure and Tools for Social Media Data</h3>
                <ul class="lead text-muted">
                <li>Argumentation Mining from Social Media Text [Computing Science, Politics] </li>
                <li>Managing Inferential Risk in the Use of Social Media Data [Computing Science, Business]</li>
                <li>Inferring Qualitative Models of Information Diffusion in Social Media Networks [Computing Science, Sociology] </li>
                <li>Provenance of Social Media Data [Computing Science, Transport]</li> <li>Tools to Support Social Scientists in Collecting and Linking Social Media Data [Computing Science, Sociology) </li>
                <li>Opinion Diffusion in a Connected World [Computing Science, Management]</li>
                </ul>

                <h3 class="section-header">Training &amp; Capacity Building</h3>
                <p class="lead text-muted">Workshops in Aberdeen for external PhD students and early career researchers directly drawing on experiences of projects above.</p>

                <h3 class="section-header">Public Engagement</h3>

                <p class="lead text-muted">Festivals Tour Explores the views of the public on issues related to social media through interactive activities related to the work above at festivals: food, music and science.</p>

                <p class="lead text-muted"><small>Social Media&#58; Developing Understanding, Infrastructure &amp; Engagement is supported by the Economic and Social Research Council [ES/M001628/1]</small></p>
